console.log('Addition of various types:')
console.log('1 (String)     + true (Boolean) = ', '1' + true);
console.log('1 (String)     + 2 (Number)     = ', '1' + 2);
console.log('true (Boolean) + 2 (Number)     = ', true + 2, '\n');

console.log('Multiplication of various types:')
console.log('1 (String)     * true (Boolean) = ', '1' * true);
console.log('1 (String)     * 2 (Number)     = ', '1' * 2);
console.log('true (Boolean) * 2 (Number)     = ', true * 2, '\n');

console.log('Divide different types:')
console.log('1 (String)     / true (Boolean) = ', '1' / true);
console.log('1 (String)     / 2 (Number)     = ', '1' / 2);
console.log('true (Boolean) / 2 (Number)     = ', true / 2, '\n');

console.log('Explicit conversion:')
console.log('Number(true) = ', Number(true));
console.log('String(2)    = ', String(2));
console.log('Boolean(2)   = ', Boolean(2));